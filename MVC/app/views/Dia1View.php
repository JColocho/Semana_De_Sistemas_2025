                <h2>¿Qué es la Inteligencia Artificial?</h2>
                <p>Se inició la sesión definiendo la Inteligencia Artificial (IA) como la 
                    ingeniería de hacer que las máquinas piensen y actúen como los humanos, 
                    capaces de resolver problemas, reconocer patrones y tomar decisiones.
                    La IA combina conocimiento, aprendizaje y capacidad de adaptación para 
                    ejecutar tareas que antes solo eran posibles mediante la intervención humana.</p>

                <h2>Clasificación de la IA según su capacidad</h2>
                <p>Se presentaron las dos categorías principales de inteligencia artificial:</p>
                <ul>
                    <li><p>IA débil: Diseñada para realizar tareas específicas, como los asistentes virtuales, sistemas de recomendación o chatbots.</p></li>
                    <li><p>IA fuerte: Un modelo teórico capaz de razonar y aprender de forma similar a un ser humano, con conciencia y comprensión profunda.</p></li>
                </ul>

                <h2>Diferencias entre enfoques de aprendizaje</h2>
                <p>Durante la ponencia se diferenciaron los principales tipos de aprendizaje dentro de la IA:</p>
                <ul>
                    <li><p>Machine Learning: Permite a los sistemas aprender de los datos sin ser programados explícitamente.</p></li>
                    <li><p>Deep Learning: Se basa en redes neuronales con múltiples capas que imitan el funcionamiento del cerebro humano, ideales para tareas como el reconocimiento de voz o imágenes.</p></li>
                    <li><p>Foundation Learning: Alude a los modelos fundacionales, grandes modelos preentrenados que sirven como base para múltiples aplicaciones, como los Large Language Models (LLM).</p></li>
                </ul>

                <h2>Redes Generativas Antagónicas (GAN)</h2>
                <div class="bloque-actividad-centro">
                    <img src="img/dia1/ponencia2/GAN.jpg" alt="GAN">
                </div>
                <p>
                    Uno de los conceptos más llamativos fue el de las Redes Generativas Antagónicas (GAN), 
                    un tipo de arquitectura que utiliza dos redes neuronales —una generadora y otra 
                    discriminadora— que compiten entre sí para producir resultados cada vez más realistas.
                    Gracias a las GAN, hoy es posible crear imágenes, voces y videos que parecen auténticos, transformando el ámbito del arte digital, el diseño y la simulación.
                </p>

