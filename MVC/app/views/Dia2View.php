                <p>La segunda ponencia del día presentó una introducción práctica al framework <strong>Laravel</strong> junto con <strong>Filament</strong>, una potente herramienta para crear paneles de administración modernos y CRUDs en cuestión de minutos. A través de una demostración en vivo, se mostró cómo construir una aplicación completa con funcionalidades administrativas sin necesidad de código complejo.</p>

                <h2>¿Qué es Filament?</h2>
                <p><strong>Filament</strong> es un panel de administración desarrollado sobre Laravel, diseñado para simplificar la creación de dashboards, formularios, y operaciones CRUD. Su principal ventaja es su facilidad de uso, su diseño moderno y su alto nivel de personalización.</p>

                <h3>Características clave de Filament</h3>
                <ul>
                    <li><p>Interfaz moderna y adaptable gracias a <strong>Tailwind CSS</strong>.</p></li>
                    <li><p>CRUDs automáticos con configuración mínima.</p></li>
                    <li><p>Panel de administración preconfigurado y extensible.</p></li>
                    <li><p>Soporte integrado para formularios, tablas, relaciones, filtros y acciones personalizadas.</p></li>
                    <li><p>Ampliable mediante plugins y componentes propios.</p></li>
                </ul>

                <div class="bloque-actividad-centro my-3">
                    <img src="img/dia2/ponencia2/filament.jpg" alt="Panel de Filament">
                </div>

                <h2>Instalación de Laravel</h2>
                <p>El proceso comenzó con la creación de un nuevo proyecto Laravel, utilizando Composer para instalar las dependencias y configurar el entorno:</p>
                <pre><code>composer global require laravel/installer</code></pre>

                <h2>Instalación de Filament</h2>
                <p>Para añadir Filament al proyecto, se ejecutó el siguiente comando:</p>
                <pre><code>composer require filament/filament: "^4.0"</code></pre>
                <p>Después de la instalación, se generó el panel de administración con:</p>
                <pre><code>php artisan filament:install --panels</code></pre>
                <p>Esto permitió acceder a un dashboard funcional con autenticación lista para usar.</p>


                <h2>Creación de módulos</h2>
